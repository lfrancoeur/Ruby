numbers = [0, 2, 4, 8, 10, 12, 14]

p numbers.slice(3) #shows the 3rd index position
p numbers.slice(100) #shows a nill
p numbers.slice(2, 3) #Starts at 2 and adds 3 indexes (shows index 2,3,4)
p numbers.slice(4..6) #starts at 4 and ends at index 6
p numbers.slice(4...6) #starts at 4 and ends at 5 
